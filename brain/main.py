async def handle_telegram_message(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """Main Telegram entry point."""
    user_msg = update.message.text
    chat_id = update.effective_chat.id
    
    await update.message.reply_text("🤖 Processing your request... (Routing to Brain)")
    
    # 1. Route to AI Brain
    ai_content = await model_router(user_msg)
    
    # 2. Call WP Gateway to create DRAFT
    wp_result = await call_wp_tool("manage_posts", {
        "action": "create",
        "title": ai_content["title"],
        "content": ai_content["content"]
    })
    
    if "error" in wp_result:
        await update.message.reply_text(f"❌ WP Error: {wp_result['error']}")
        return

    post_id = wp_result["post_id"]
    preview_url = wp_result.get("url")

    # 3. Store for approval
    pending_approvals[chat_id] = {
        "post_id": post_id,
        "content": ai_content
    }

    # 4. Request Approval with Buttons
    keyboard = [
        [
            InlineKeyboardButton("✅ Publish Now", callback_data=f"publish_{post_id}"),
            InlineKeyboardButton("🗑️ Discard", callback_data=f"discard_{post_id}")
        ]
    ]
    reply_markup = InlineKeyboardMarkup(keyboard)
    
    await update.message.reply_text(
        f"📝 **Draft Created!**\n\n"
        f"Title: {ai_content['title']}\n"
        f"Preview: {preview_url}\n\n"
        f"Should I publish this to your site?",
        reply_markup=reply_markup,
        parse_mode="Markdown"
    )

async def button_callback(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """Handles 'Publish' or 'Discard' button clicks."""
    query = update.callback_query
    await query.answer()
    
    data = query.data
    chat_id = update.effective_chat.id
    
    if data.startswith("publish_"):
        post_id = data.split("_")[1]
        # Update WP status to 'publish'
        result = await call_wp_tool("manage_posts", {
            "action": "edit",
            "post_id": int(post_id),
            "status": "publish"
        })
        
        if "success" in result:
            await query.edit_message_text(text=f"🚀 **Success!** Post #{post_id} is now LIVE.")
        else:
            await query.edit_message_text(text=f"❌ Failed to publish: {result.get('error')}")
            
    elif data.startswith("discard_"):
        await query.edit_message_text(text="🗑️ Draft discarded.")

