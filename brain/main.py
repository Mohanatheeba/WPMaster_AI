async def call_wp_tool(tool: str, params: dict):
    """Calls the WordPress Gateway Plugin REST API."""
    url = f"{WP_URL}/tools"
    auth = (WP_USERNAME, WP_APP_PASSWORD)
    payload = {
        "tool": tool,
        "params": params
    }
    
    response = requests.post(url, json=payload, auth=auth)
    return response.json()

